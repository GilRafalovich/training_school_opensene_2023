# The radius depends on both the network density and the typical spatial correlation of rainfall in a certain region. The more small-scale und convective-driven rainfall is, the smaller the radius should be chosen, if network density allows for it. 15 km worked well in various studies.

# Lower radius:
# * less CMLs can be used of rainfall retrieval
# * local events can potentially get picked up better
# * outliers are detected less good

# Higher radius:
# * more CMLs can be processed
# * outlier detection works better, until very small rain events are detected as outliers